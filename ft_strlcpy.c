#include "libft.h"

size_t	ft_strlcpy(char *d, const char *s, size_t n)
{
	size_t	i;
	size_t	j;

	i = 0;
	while (s[i])
		i++;
	if (n > 0)
	{
		j = 0;
		while (s[j] && j < n - 1)
		{
			d[j] = s[j];
			j++;
		}
		d[j] = '\0';
	}
	return (i);
}
